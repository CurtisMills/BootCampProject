    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        $user = Auth::user();
        $usersLiked = $user->likes->pluck('liked_id');
        // Users I have liked.
        $likedByUsers = $user->hasLiked->pluck('user_id');
        // Users that have liked me.

        $matches = $usersLiked->intersect($likedByUsers);

        $users = User::whereIn('id', $matches)->get();

        return view('matches.index', compact('users'));
    }
    

